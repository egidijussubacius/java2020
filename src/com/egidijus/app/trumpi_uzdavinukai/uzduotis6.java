package com.egidijus.app;
//6. Write a Java program to print the sum (addition), multiply, subtract, divide and remainder of two numbers. Go to the editor
//Test Data:
//Input first number: 125
//Input second number: 24

public class uzduotis6 {
    public static void main(String[] args) {
        int num1 = 125;
        int num2 = 24;
        System.out.println("125 + 24 = " +(num1 + num2));
        System.out.println("125 - 24 = " +(num1 - num2));
        System.out.println("125 * 24 = " +(num1 * num2));
        System.out.println("125 / 24 = " +(num1 / num2));
        System.out.println("125 mod 24 = " +(num1 % num2));
    }
}
